package org.eclipse.mylyn.docs.intent.client.ui.editor.quickfix;

import org.eclipse.compare.CompareConfiguration;
import org.eclipse.emf.ecore.resource.Resource;
import org.eclipse.mylyn.docs.intent.client.ui.IntentEditorActivator;
import org.eclipse.swt.graphics.Image;

/**
 * {@link CompareConfiguration} specifying how compare editors should be displayed.
 * 
 * @author <a href="mailto:alex.lagarde@obeo.fr">Alex Lagarde</a>
 */
public class IntentCompareConfiguration extends CompareConfiguration {

	private Resource workingCopyResource;

	/**
	 * Default constructor.
	 * 
	 * @param generatedResource
	 *            the resource to compare (generated by Intent)
	 * @param workingCopyResource
	 *            the resource to compare (used by user in working copy)
	 */
	public IntentCompareConfiguration(Resource generatedResource, Resource workingCopyResource) {
		this.workingCopyResource = workingCopyResource;
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.compare.CompareConfiguration#getLeftLabel(java.lang.Object)
	 */
	@Override
	public String getLeftLabel(Object element) {
		return "Intent Document";
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.compare.CompareConfiguration#getLeftImage(java.lang.Object)
	 */
	@Override
	public Image getLeftImage(Object element) {
		return IntentEditorActivator.getDefault().getImage("icon/outline/document.gif");
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.compare.CompareConfiguration#getRightLabel(java.lang.Object)
	 */
	@Override
	public String getRightLabel(Object element) {
		return "Working Copy (" + workingCopyResource.getURI() + ")";
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.compare.CompareConfiguration#isLeftEditable()
	 */
	@Override
	public boolean isLeftEditable() {
		return false; // TODO check consistency
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.compare.CompareConfiguration#isRightEditable()
	 */
	@Override
	public boolean isRightEditable() {
		return true;
	}
}
